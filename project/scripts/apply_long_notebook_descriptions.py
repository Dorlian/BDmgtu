def to_notebook_source(text: str) -> list[str]:
    text = text.replace("\r\n", "\n").replace("\r", "\n")
    if not text.endswith("\n"):
        text += "\n"
    lines = text.split("\n")
    # split даёт финальный пустой элемент из-за завершающего \n
    return [line + "\n" for line in lines[:-1]]


def patch_starts_with_code_marker(nb: dict, replacements: list[str]) -> None:
    idx = 0
    for cell in nb["cells"]:
        if cell.get("cell_type") != "markdown":
            continue
        s = "".join(cell.get("source", []))
        if not re.match(r"^\s*\*\*Код:\*\*", s):
            continue
        if idx >= len(replacements):
            raise RuntimeError(f"Слишком много маркеров **Код:** (ожидалось {len(replacements)})")
        cell["source"] = to_notebook_source(replacements[idx])
        idx += 1
    if idx != len(replacements):
        raise RuntimeError(f"Найдено маркеров {idx}, ожидалось {len(replacements)}")


